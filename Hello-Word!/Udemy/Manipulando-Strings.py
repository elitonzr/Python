# -*- coding: utf-8 -*-

t = "manipulando strings!"

# Para deixar todas letras em maiusculo usar .upper() por exemplo:
t = t.upper()
print(t)

# Para deixar todas as letras em minusculo usar .lower() por exemplo:
t = t.lower()
print(t)

# Para deixar a primeira letra maiuscula eo restante minusculo usar .capitalize() por exemplo:
t = t.capitalize()
print(t)

# Para mudar de maiuscula para minuscula ou vise versa usar .swapcase() por exemplo:
t = t.swapcase()
print(t)
